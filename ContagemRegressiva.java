
public class ContagemRegressiva {

	public static void main(String[] args) {
		
		System.out.println("Contagem Regressiva");
		for (byte i = 9; i > 0; i--)
		{
			System.out.println(i);			
		}
		
		System.out.println("FOI!");
		
		for (byte i = 0; i > 0; i--)
		{
			System.out.println(i);			
		}
	}
}